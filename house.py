import turtle
t=turtle.Turtle()
t.width(10)
t.fillcolor("lawngreen")
t.begin_fill()
for i in range(4):
    t.fd(150)
    t.rt(90)
t.end_fill()
t.lt(60)
t.fillcolor("yellow")
t.begin_fill()
for i in range(2):
    t.fd(150)
    t.rt(120)
t.end_fill()
t.color("red")
t.up()
t.goto(100,80)
t.down()
t.rt(90)
t.fd(60)
t.rt(90)
t.fd(20)
t.rt(90)
t.fd(85)

t.up()
t.goto(50,-50)
t.down()
t.color("blue")
t.fillcolor("blue")
t.begin_fill()
t.circle(20)
t.end_fill()

